function countItems(roadmap) {
  return (
    roadmap?.modules?.reduce((count, module) => count + module.items.length, 0) ||
    0
  );
}

export function buildRoadmapStats(roadmap) {
  const items = countItems(roadmap);

  if (roadmap?.numbering === "day") {
    return [
      { label: "Days", value: String(items) },
      { label: "Problems", value: String(items) },
      { label: "Practice mix", value: "LC + CF" },
    ];
  }

  return [
    { label: "Projects", value: String(roadmap?.modules?.length || 0) },
    { label: "Milestones", value: String(items) },
    { label: "Code given", value: "0" },
  ];
}

export function getDefaultRoadmap(slug) {
  const roadmap = defaultRoadmaps[slug];
  if (!roadmap) return null;

  return {
    ...roadmap,
    stats: buildRoadmapStats(roadmap),
  };
}
